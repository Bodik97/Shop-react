// src/utils/dispatch.test.js
import { describe, it, expect } from "vitest";
import { dispatchMessage } from "./dispatch";

// Дати в UTC; Київ улітку = UTC+3, тому 10:00 UTC = 13:00 Києва.
// 2026-07-06 — понеділок.
const kyivDate = (isoUtc) => new Date(isoUtc);

describe("dispatchMessage", () => {
  it("будній день до 15:00 — відправимо сьогодні", () => {
    // Пн 13:00 Києва (10:00 UTC)
    expect(dispatchMessage(kyivDate("2026-07-06T10:00:00Z"))).toBe(
      "Замовте до 15:00 — відправимо сьогодні"
    );
  });

  it("будній день після 15:00 — відправимо завтра", () => {
    // Пн 16:00 Києва (13:00 UTC)
    expect(dispatchMessage(kyivDate("2026-07-06T13:00:00Z"))).toBe(
      "Замовте зараз — відправимо завтра"
    );
  });

  it("субота до 13:00 — відправимо сьогодні", () => {
    // Сб 2026-07-11, 12:00 Києва (09:00 UTC)
    expect(dispatchMessage(kyivDate("2026-07-11T09:00:00Z"))).toBe(
      "Замовте до 13:00 — відправимо сьогодні"
    );
  });

  it("субота після 13:00 — відправимо в понеділок", () => {
    // Сб 14:00 Києва (11:00 UTC)
    expect(dispatchMessage(kyivDate("2026-07-11T11:00:00Z"))).toBe(
      "Замовте зараз — відправимо в понеділок"
    );
  });

  it("неділя — відправимо завтра (понеділок)", () => {
    // Нд 2026-07-12, 12:00 Києва (09:00 UTC)
    expect(dispatchMessage(kyivDate("2026-07-12T09:00:00Z"))).toBe(
      "Замовте зараз — відправимо завтра"
    );
  });

  it("межа таймзони: пізній вечір UTC = наступний день у Києві", () => {
    // Нд 23:00 UTC = Пн 02:00 Києва → будній до 15:00 → сьогодні
    expect(dispatchMessage(kyivDate("2026-07-05T23:00:00Z"))).toBe(
      "Замовте до 15:00 — відправимо сьогодні"
    );
  });
});
