// Обкладинка = фото товару з тієї ж категорії (переюз асета).
async function coverRef(category) {
  const ref = await client.fetch(
    `*[_type == "product" && category == $category && defined(mainImage.asset)][0].mainImage.asset._ref`,
    { category }
  );
  return ref ? { _type: "image", asset: { _type: "reference", _ref: ref } } : undefined;
}

let ok = 0;
for (const post of posts) {
  try {
    const mainImage = await coverRef(post.coverCategory || post.category);
    const doc = {
      _id: post._id,
      _type: "post",
      title: post.title,
      slug: { _type: "slug", current: post.slug },
      excerpt: post.excerpt,
      seoTitle: post.title,
      author: AUTHOR,
      authorRole: ROLE,
      category: post.category,
      published: true,
      publishedAt: post.publishedAt,
      body: post.body,
      faq: post.faq,
      ...(mainImage ? { mainImage } : {}),
    };
    const res = await client.createOrReplace(doc);
    ok++;
    console.log(`✓ ${res._id}  →  /blog/${post.slug}${mainImage ? "" : "  (без обкладинки — немає фото в категорії)"}`);
  } catch (e) {
    console.error(`✗ ${post._id}:`, e?.message || e);
  }
}
console.log(`\nГотово: ${ok}/${posts.length} статей.`);
